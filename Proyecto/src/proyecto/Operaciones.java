public class Operaciones {
    public int Suma(int x, int y){
        return (x+y);
    }
    
    public int Resta(int x, int y){
        return (x-y);
    }
    
    public int Multi(int x, int y){
        return (x*y);
    }
    
    public int Div(int x, int y){
        return (x/y);
    }
}
